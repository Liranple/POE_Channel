export function isSupportGem(gemName) {
  return gemName.includes("Support") || gemName.includes("보조");
}

// 각성 젬인지 확인
export function isAwakenedGem(gemName) {
  return gemName.includes("Awakened") || gemName.includes("각성한");
}

// 바알 젬인지 확인
export function isVaalGem(gemName) {
  return gemName.startsWith("Vaal") || gemName.startsWith("바알");
}
